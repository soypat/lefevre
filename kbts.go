package lefevre

import "unicode/utf8"

// fourcc creates a four-character code from four bytes, matching the KBTS_FOURCC macro.
func fourcc(a, b, c, d byte) uint32 {
	return uint32(a) | uint32(b)<<8 | uint32(c)<<16 | uint32(d)<<24
}

// Direction represents the directionality of text.
type Direction uint8

const (
	DirectionUnknown Direction = iota
	DirectionLTR
	DirectionRTL
)

// Reverse returns the opposite direction. Unknown stays Unknown.
func (d Direction) Reverse() Direction {
	switch d {
	case DirectionLTR:
		return DirectionRTL
	case DirectionRTL:
		return DirectionLTR
	default:
		return DirectionUnknown
	}
}

// String returns "LTR", "RTL", or "Unknown".
func (d Direction) String() string {
	switch d {
	case DirectionLTR:
		return "LTR"
	case DirectionRTL:
		return "RTL"
	default:
		return "Unknown"
	}
}

// BreakFlags is a bitfield indicating what type of break occurred at a position.
type BreakFlags uint8

const (
	BreakFlagDirection          BreakFlags = 1 << iota // Direction change (LTR <-> RTL)
	BreakFlagScript                                    // Script change
	BreakFlagGrapheme                                  // Grapheme cluster boundary
	BreakFlagWord                                      // Word boundary
	BreakFlagLineSoft                                  // Optional line break point
	BreakFlagLineHard                                  // Mandatory line break (newline/paragraph)
	BreakFlagManual                                    // User-requested break
	BreakFlagParagraphDirection                        // Paragraph direction change

	BreakFlagLine = BreakFlagLineSoft | BreakFlagLineHard
	BreakFlagAny  = BreakFlagDirection | BreakFlagScript | BreakFlagGrapheme | BreakFlagWord | BreakFlagLineSoft | BreakFlagLineHard
)

// HasAll returns true if f contains all bits in flag.
func (f BreakFlags) HasAll(flagsQuery BreakFlags) bool { return f&flagsQuery == flagsQuery }

// IsLineBreak returns true if either soft or hard line break flag is set.
func (f BreakFlags) IsLineBreak() bool { return f&BreakFlagLine != 0 }

// IsHardBreak returns true if the hard line break flag is set.
func (f BreakFlags) IsHardBreak() bool { return f&BreakFlagLineHard != 0 }

