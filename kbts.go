// Font is a parsed font resource. Create with FontFromMemory.
type Font struct {
	data       []byte                 // retained reference to raw font bytes; must not be modified after parse
	tables     [tableCount]tableEntry // parsed table directory
	cmapFormat uint16                 // selected cmap subtable format (4 or 12)
	cmapOffset uint32                 // absolute offset of selected cmap subtable in data
	err        error                  // sticky parse error
}

// FontFromMemory parses a font from raw TTF/OTF/TTC bytes.
// fontIndex selects which font in a TTC/OTC collection (0-based). For single .ttf/.otf files, use 0.
func FontFromMemory(data []byte, fontIndex int) (*Font, error) {
	return fontFromMemory(data, fontIndex)
}

// FontCount returns the number of fonts in a TTC/OTF/TTF byte slice.
// Returns 0 for invalid or empty data.
func FontCount(data []byte) int {
	return fontCount(data)
}

// IsValid reports whether the font was successfully parsed.
func (f *Font) IsValid() bool {
	return f != nil && f.err == nil && f.data != nil
}

// Info returns metadata about the font.
// Returns zero FontInfo if the font is invalid.
func (f *Font) Info() FontInfo {
	return f.fontInfo()
}

// GlyphID returns the glyph index for a codepoint via the font's cmap table.
// Returns 0 (.notdef) if the codepoint is not mapped or the font is invalid.
func (f *Font) GlyphID(codepoint rune) uint16 {
	return f.glyphID(codepoint)
}

// BreakConfigFlags controls break generation behavior.
type BreakConfigFlags uint8

const (
	BreakConfigNone                        BreakConfigFlags = 0
	BreakConfigEndOfTextGeneratesHardBreak BreakConfigFlags = 1
)

// Breaker is a high-level streaming text segmenter.
// Feed text via AppendBreak, then call End to flush remaining breaks.
// The zero value is ready to use with default settings.
type Breaker struct {
	Direction Direction
	JStyle    JapaneseLineBreakStyle
	Flags     BreakConfigFlags
	state     BreakState
	begun     bool
}

// AppendBreak processes all complete runes in text, appending breaks to dst.
// Returns the extended slice and the number of bytes consumed.
// Incomplete trailing UTF-8 sequences are not consumed.
// Never sets end-of-text; call End after the last chunk.
func (b *Breaker) AppendBreak(dst []Break, text []byte) ([]Break, int) {
	if !b.begun {
		b.state.Begin(b.Direction, b.JStyle, b.Flags)
		b.begun = true
	}
	var bytesConsumed int
	for bytesConsumed < len(text) {
		r, size := utf8.DecodeRune(text[bytesConsumed:])
		if r == utf8.RuneError && size <= 1 {
			break // incomplete or invalid trailing byte
		}
		bytesConsumed += size
		b.state.AddCodepoint(r, 1, false)
		for {
			br, ok := b.state.Next()
			if !ok {
				break
			}
			dst = append(dst, br)
		}
	}
	return dst, bytesConsumed
}

// End signals end-of-text and flushes remaining breaks to dst.
// After End, the Breaker can be reused for new text.
func (b *Breaker) End(dst []Break) []Break {
	b.state.End()
	for {
		br, ok := b.state.Next()
		if !ok {
			break
		}
		dst = append(dst, br)
	}
	b.begun = false
	return dst
}

// BreakState is a streaming text segmentation state machine.
// Zero value is valid; call Begin before feeding codepoints.
type BreakState struct {
	s breakStateInternal
}

// GuessTextProperties scans text for the first strong directional character
// and first identifiable script. Useful for mono-script, mono-direction text.
func GuessTextProperties(text string) (Direction, Script) {
	script := ScriptUnknown
	dir := DirectionUnknown

	if len(text) == 0 {
		return DirectionLTR, ScriptUnknown
	}

	var bs BreakState
	bs.Begin(DirectionUnknown, JapaneseLineBreakNormal, BreakConfigNone)
	for i, r := range text {
		// Check if this is the last rune by seeing if the next byte position is at or past the end.
		eot := i+utf8.RuneLen(r) >= len(text)
		bs.AddCodepoint(r, 1, eot)
		for {
			b, ok := bs.Next()
			if !ok {
				break
			}
			if script == ScriptUnknown && b.Flags.HasAll(BreakFlagScript) {
				script = b.Script
			}
			if dir == DirectionUnknown && b.Flags.HasAll(BreakFlagDirection) {
				dir = b.Direction
			}
		}
		if script != ScriptUnknown && dir != DirectionUnknown {
			break
		}
	}

	if dir == DirectionUnknown {
		dir = DirectionLTR
	}
	return dir, script
}
