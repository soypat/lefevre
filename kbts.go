// ShapeError represents errors during shaping.
type ShapeError uint8

const (
	ShapeErrorNone ShapeError = iota
	ShapeErrorInvalidFont
	ShapeErrorTextBeforeBegin
	ShapeErrorOutOfMemory
)

// Error implements the error interface.
func (e ShapeError) Error() string {
	switch e {
	case ShapeErrorNone:
		return "no error"
	case ShapeErrorInvalidFont:
		return "invalid font"
	case ShapeErrorTextBeforeBegin:
		return "text given before calling Begin"
	case ShapeErrorOutOfMemory:
		return "out of memory"
	default:
		return "unknown shape error"
	}
}

